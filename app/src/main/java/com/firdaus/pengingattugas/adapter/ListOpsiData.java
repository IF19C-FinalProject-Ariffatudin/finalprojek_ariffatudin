package com.firdaus.pengingattugas.adapter;


public class ListOpsiData {
    private int icon;
    private String iconNama;

    public ListOpsiData(int icon, String iconNama) {
        this.icon = icon;
        this.iconNama = iconNama;
    }

    public int getIcon() {
        return icon;
    }

    public String getIconNama() {
        return iconNama;
    }
}
